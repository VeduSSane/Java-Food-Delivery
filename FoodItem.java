class FoodItem 
{
    String name;
    double price;
//constructor overloading
    FoodItem(String name, float price) 
  {
        this.name = name;
        this.price = price;
  }

    FoodItem(String name, double price) 
  {
        this.name = name;
        this.price = price;
  }
}//End of class FoodItem
